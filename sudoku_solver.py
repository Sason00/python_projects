def check_same_row_column(n, c, r, board):
    for i in range(len(board[r])):
        if n in board[r]:
            return False
    for i in range(len(board)):
        if board[i][c] == n and r != i:
            return False
    x = c // 3
    y = r // 3
    for i in range(y * 3, y * 3 + 3):
        for j in range(x * 3, x * 3 + 3):
            if board[i][j] == n and (i, j) != (x, y):
                return False
    return True

def find_empty_place(board):
    for i in range(len(board)):
        for j in range(len(board[0])):
            if board[i][j] == 0:
                return (i, j)
    return None

